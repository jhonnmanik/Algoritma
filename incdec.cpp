#include <iostream>

using namespace std;

int main()
{
    // Increment dan decremment
    // preincrement dan postincrement

    int a = 5;
    int b = 5;

    // postincrement
    cout << a << endl;
    a++;
    cout << a << endl << endl;

    // preincrement
    cout << b << endl;
    ++b;
    cout << b << endl;

    cin.get();
    return 0;
}